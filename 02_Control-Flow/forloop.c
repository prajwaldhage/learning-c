// Write a Program to display the word "Computer" five times using for loop
// Day 8: For Loop
// Date: 26-12-2025

#include <stdio.h>
void main()
{
    int i = 1;
    for(i=1;i<=5;i++)
    {
        printf("Computer\n");
    }
}

// Output:

// Computer
// Computer
// Computer
// Computer
// Computer