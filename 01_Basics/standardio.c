#include <stdio.h>
int main()
{
    int length, breadth, area;
    printf("Enter Length: ");
    scanf("%d", &length);
    printf("Enter Breadth: ");
    scanf("%d", &breadth);
    area = length * breadth;
    printf("Area: %d", area);
}

